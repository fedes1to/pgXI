	public static string TagOfFirstUpgrade(string tg)
	{
		if (tg == null)
		{
			return null;
		}
		int count = upgrades.Count;
		for (int i = 0; i != count; i++)
		{
			List<string> list = upgrades[i];
			if (list.Contains(tg))
			{
				return list[0];
			}
		}
		return tg;
	}

